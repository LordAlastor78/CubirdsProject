/**
 * ENUM TypeBird — Las 8 especies de pájaros disponibles en el juego
 * 
 * ¿QUÉ ES?
 * - Una enumeración con los tipos de pájaros del juego
 * - Cada constante es una especie diferente
 * - NO tiene propiedades especiales (como "número de cartas" o "bandada")
 * → Eso está en la clase Card
 * 
 * ¿CÓMO SE USA?
 * - Para indicar qué especie es una carta: card.getTypeBird()
 * - Para iterar todas las especies: TypeBird.values()
 * - Para comparar si dos cartas son del mismo tipo: card1.getTypeBird() ==
 * card2.getTypeBird()
 * 
 * EJEMPLO EN CÓDIGO:
 * TypeBird pajaro = TypeBird.FLAMENCO;
 * if (pajaro == TypeBird.FLAMENCO) {
 * System.out.println("Es un flamenco!");
 * }
 * 
 * ¿CÓMO MODIFICAR?
 * - Si quieres AÑADIR UN NUEVO PÁJARO:
 * 1. Añade la constante aquí (ej: AGUILA)
 * 2. Añade cartas en Card.java (AGUILA_1, AGUILA_2, etc.)
 * 3. El array speciesCounters en Player se ajusta automáticamente
 */
public enum TypeBird {
    FLAMENCO, // 0 en ordinal()
    TUCAN, // 1 en ordinal()
    PATO, // 2 en ordinal()
    URRACA, // 3 en ordinal()
    PETIRROJO, // 4 en ordinal()
    LECHUZA, // 5 en ordinal()
    CURRUCA, // 6 en ordinal()
    GUACAMAYO; // 7 en ordinal() — Total: 8 especies
}

